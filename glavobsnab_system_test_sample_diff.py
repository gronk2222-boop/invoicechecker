"""
Скрипт для создания тестовых Excel файлов
"""
import pandas as pd

# Создаем тестовую заявку
request_data = {
    'Наименование': [
        'Цемент М500',
        'Песок строительный',
        'Щебень фр. 20-40',
        'Арматура А500С d12',
        'Кирпич полнотелый М150'
    ],
    'Количество': [20, 10, 15, 100, 500],
    'Ед. изм.': ['мешков', 'м3', 'м3', 'м.п.', 'шт'],
    'Цена за ед.': [550, 1800, 2200, 45, 18],
}

request_df = pd.DataFrame(request_data)
request_df['Сумма'] = request_df['Количество'] * request_df['Цена за ед.']
request_df.to_excel('test_request.xlsx', index=False)
print('✓ Создан test_request.xlsx')

# Создаем тестовый счет от поставщика 1
